const fs = require('fs');
const path = require('path');

function walk(dir, fileList = []) {
  const entries = fs.readdirSync(dir, { withFileTypes: true });
  for (const entry of entries) {
    const full = path.join(dir, entry.name);
    if (entry.isDirectory()) walk(full, fileList);
    else if (/\.(c|cpp|cc|cxx|h|hpp)$/.test(entry.name)) fileList.push(full);
  }
  return fileList;
}

function relativePosix(p, base) {
  return path.relative(base, p).split(path.sep).join('/');
}

function main() {
  const firmwareRoot = path.resolve(__dirname, '..');
  const searchDirs = [path.join(firmwareRoot, 'include'), path.join(firmwareRoot, 'src')];
  const files = [];
  for (const d of searchDirs) {
    if (fs.existsSync(d)) walk(d, files);
  }

  files.sort();

  const distDir = path.join(firmwareRoot, 'dist');
  if (!fs.existsSync(distDir)) fs.mkdirSync(distDir, { recursive: true });
  const outFile = path.join(distDir, 'integrated_firmware.cpp');

  const header = `// Integrated firmware source
// Generated: ${new Date().toISOString()}
// Do not edit this file; it's a concatenation of the firmware sources for review or archiving.
\n`;

  let out = header;
  for (const f of files) {
    const rel = relativePosix(f, firmwareRoot);
    out += `\n// ----- Begin: ${rel} -----\n`;
    let content = fs.readFileSync(f, 'utf8');
    out += content + `\n// ----- End:   ${rel} -----\n`;
  }

  fs.writeFileSync(outFile, out, 'utf8');
  console.log(`Wrote integrated firmware to ${outFile}`);
  console.log(`Included ${files.length} files.`);
}

main();
